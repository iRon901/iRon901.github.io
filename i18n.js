const LANGS = ['en','zh','zhs','ja','ko','vi','th','hi','ar','de','es','pt'];

function getLang() { return localStorage.getItem('cy_lang') || 'en'; }
function saveLang(l) { localStorage.setItem('cy_lang', l); }

function applyLang(lang) {
  if (!I18N[lang]) lang = 'en';
  const t = I18N[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });
  const flagEl = document.getElementById('currentFlag');
  const codeEl = document.getElementById('currentLang');
  if (flagEl) flagEl.textContent = t.flag;
  if (codeEl) codeEl.textContent = t.code;
  document.querySelectorAll('.lang-option').forEach((opt, i) => {
    opt.classList.toggle('active', LANGS[i] === lang);
  });
  document.documentElement.dir = t.dir || 'ltr';
  document.documentElement.lang = lang;
  saveLang(lang);
}

function setLang(lang) {
  applyLang(lang);
  const sw = document.getElementById('langSwitcher');
  if (sw) sw.classList.remove('open');
}

function toggleLang() {
  const sw = document.getElementById('langSwitcher');
  if (sw) sw.classList.toggle('open');
}

document.addEventListener('click', e => {
  const sw = document.getElementById('langSwitcher');
  if (sw && !sw.contains(e.target)) sw.classList.remove('open');
});
